package model;

/**
 *
 * @author ThanhLich
 */
public class Products {

    private int id;
    private String name;
    private String image;
    private boolean status;
    private int price;
    private int quantity;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public Products() {
    }

    public Products(int id, String name, String image, boolean status, int price, int quantity) {
        this.id = id;
        this.name = name;
        this.image = image;
        this.status = status;
        this.price = price;
        this.quantity = quantity;
    }

}
